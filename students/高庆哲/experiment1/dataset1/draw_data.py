def draw(filename):
    file = open(filename, 'r')

    class dataset(object):
        def __init__(self):
            self.x = []
            self.y = []

    data = dict()



    while True:
        line = file.readline()

        if not line:
            break

        page = line.strip().split(',')

        if page[2] in data:
            data[page[2]].x.append(float(page[0]))
            data[page[2]].y.append(float(page[1]))
        else:
            data[page[2]] = dataset()

            data[page[2]].x.append(float(page[0]))
            data[page[2]].y.append(float(page[1]))

    a = 0

    fig1 = plt.figure(1)
    for i in data:
        plt.scatter(data[i].x,data[i].y,c =cset[a],marker=markerset[a])
        a += 1
    plt.savefig('result/fig/' + filename[7:-4] + '.png')
    # plt.show()
    plt.close('all')

